# Use the interpreter to calculate how many seconds are in a year.
# Then write the code in this script file below this line.
seconds_per_minute = int(60) #seconds in a minute.
minutes_per_hour = int(60) #minutes in an hour
hours_per_day = int(24) #hours in a day
days_per_year = int(365) #days a year
seconds_per_year = seconds_per_minute*minutes_per_hour*hours_per_day*days_per_year
print("Seconds in a year: ",seconds_per_year)